import { execFileSync } from 'node:child_process'
import { existsSync, mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from 'node:fs'
import { dirname, extname, join, relative, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const sourceRoot = join(projectRoot, 'src')
const sourceFont = join(projectRoot, 'node_modules/material-symbols/material-symbols-rounded.woff2')
const outputFont = join(sourceRoot, 'assets/fonts/material-symbols-rounded-subset.woff2')
const outputMap = join(sourceRoot, 'iconsets/material-symbol-codepoints.ts')
const sourceExtensions = new Set(['.scss', '.ts', '.vue'])
const codepointAliases = {
  error_outline: 'error',
  queue: 'format_list_numbered',
}

if (!existsSync(sourceFont)) {
  throw new Error('Install dependencies before generating the Material Symbols subset.')
}

function collectSourceFiles(directory) {
  return readdirSync(directory)
    .map(name => join(directory, name))
    .flatMap(path => statSync(path).isDirectory() ? collectSourceFiles(path) : [path])
    .filter(path => sourceExtensions.has(extname(path)) && path !== outputMap)
}

function queryFont(option) {
  return execFileSync('hb-info', ['--face-loader=ft', option, sourceFont], { encoding: 'utf8' })
}

const unicodeByName = new Map()
for (const line of queryFont('--list-unicodes').split('\n')) {
  const match = line.match(/^U\+([0-9A-F]+)\t([^\s]+)$/)
  if (match && !unicodeByName.has(match[2])) {
    unicodeByName.set(match[2], match[1])
  }
}

const glyphNames = new Set()
for (const line of queryFont('--list-glyphs').split('\n')) {
  const match = line.match(/^\d+\t([^\s]+)$/)
  if (match) glyphNames.add(match[1].replace(/\.fill$/, ''))
}

const sourceFiles = collectSourceFiles(sourceRoot)
const sourceText = sourceFiles.map(path => readFileSync(path, 'utf8')).join('\n')
const literalTokens = new Set(
  [...sourceText.matchAll(/(["'`])([_a-z][_a-z0-9]*)\1/g)].map(match => match[2]),
)
const iconElementTokens = new Set(
  [...sourceText.matchAll(/<v-icon\b[^>]*>\s*([_a-z][_a-z0-9]*)\s*<\/v-icon>/g)].map(match => match[1]),
)
const requestedIcons = [...new Set([...literalTokens, ...iconElementTokens, 'help'])]
  .filter(name => name.length > 1 && (glyphNames.has(name) || Object.hasOwn(codepointAliases, name)))
  .sort((left, right) => left.localeCompare(right))

const unresolvedIcons = requestedIcons.filter(name => {
  const mappedName = codepointAliases[name] ?? name
  const codepoint = unicodeByName.get(mappedName)
  return !codepoint || Number.parseInt(codepoint, 16) < 0xe000
})

const iconNames = requestedIcons.filter(name => !unresolvedIcons.includes(name))

if (!iconNames.length) {
  throw new Error('No Material Symbol names were found in the application source.')
}

const unicodeValues = [...new Set(iconNames.map(name => unicodeByName.get(codepointAliases[name] ?? name)))]
mkdirSync(dirname(outputFont), { recursive: true })

execFileSync('hb-subset', [
  '--face-loader=ft',
  `--unicodes=${unicodeValues.join(',')}`,
  '--variations=wght=300,GRAD=0,opsz=24',
  '--drop-tables=GSUB,GPOS',
  '--no-layout-closure',
  '--no-bidi-closure',
  '--no-hinting',
  '--optimize',
  `--output-file=${outputFont}`,
  sourceFont,
])

const mapEntries = iconNames.map(name => {
  const codepoint = unicodeByName.get(codepointAliases[name] ?? name).padStart(4, '0').toLowerCase()
  return `  ${JSON.stringify(name)}: "\\u${codepoint}",`
})

writeFileSync(outputMap, [
  '// Generated by scripts/subset-material-symbols.mjs. Do not edit by hand.',
  'export const materialSymbolCodepoints: Readonly<Record<string, string>> = {',
  ...mapEntries,
  '}',
  '',
].join('\n'))

const originalSize = statSync(sourceFont).size
const subsetSize = statSync(outputFont).size
const reduction = ((1 - subsetSize / originalSize) * 100).toFixed(1)

process.stdout.write([
  `Scanned ${sourceFiles.length} source files.`,
  `Included ${iconNames.length} Material Symbols.`,
  `Generated ${relative(projectRoot, outputFont)} (${(subsetSize / 1024).toFixed(1)} KiB, ${reduction}% smaller).`,
  `Generated ${relative(projectRoot, outputMap)}.`,
  ...(unresolvedIcons.length ? [`Unresolved glyphs without PUA aliases: ${unresolvedIcons.join(', ')}`] : []),
  '',
].join('\n'))
